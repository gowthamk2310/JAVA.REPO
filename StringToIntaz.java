package FullStack;

public class StringToIntaz {

	static public void  main(String args[]) {
		int vicky = 100;
		String veera = String.valueOf(vicky);
		System.out.println(veera);
		
		int vicky2 = 42;
		String veera2 = Integer.toString(vicky2);
		System.out.println(veera2);
		
		//String into int
		
		String veera3 = "300";
		int vicky3 = Integer.parseInt(veera3);
		System.out.println(vicky3);
		
		
	}
	
}
